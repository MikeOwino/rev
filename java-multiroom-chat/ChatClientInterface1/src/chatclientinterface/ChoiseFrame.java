    /**
     * Inizializza la comboBox con i nomi delle stanze presenti sul server
     */
    public void initializeRoomsComboBox(){
        String [] rooms;
        rooms=new String[ChatClientInterface.ChatRooms.size()];
        for(int i=0;i<rooms.length;i++)
            rooms[i]=(String) ChatClientInterface.ChatRooms.get(i);
        System.out.println(rooms);
        jComboBoxRooms.setModel(new javax.swing.DefaultComboBoxModel(rooms));
    }
    
    /**
     * Inizializza la lista degli utenti online 
     */
    
    public void initializeUsersList(){
        
        if(ChatClientInterface.UsersNames.size()!=0)
            jListUsers.setListData(ChatClientInterface.UsersNames.toArray());     
    }
    
    /**
     * inizializza la lista degli utenti offline
     */
    public void initializeUsersOffList(){
        
        if(ChatClientInterface.UsersNamesOff.size()!=0)
            jListUsersOff.setListData(ChatClientInterface.UsersNamesOff.toArray());     
    }
