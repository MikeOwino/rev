    /**
     * Comunica al server il nickname scelto dall'utente.
     * @param evt Evento generato al cliccare del button "Ok".
     */
    private void jButtonOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOkActionPerformed
        // TODO add your handling code here:
        if(!ChatClientInterface.UsersNames.contains(jTextFieldName.getText()) && !ChatClientInterface.UsersNamesOff.contains(jTextFieldName.getText())){
            ChatClientInterface.name=jTextFieldName.getText();
            String msg="IUN"+ChatClientInterface.name+"⧫"+ChatClientInterface.nClient;
            ChatClientInterface.pw.println(msg);
            jButtonOk.setEnabled(false);
            jLabelNickname.setEnabled(false);
            jTextFieldName.setEnabled(false);
            jLabel2.setEnabled(true);
            jComboBoxRooms.setEnabled(true);
            jButtonNext.setEnabled(true);
            jButtonNewRoom.setEnabled(true);
            
        }
        else{
            JOptionPane.showMessageDialog(null,"Nickename already exists, choose another one","ERRORE",JOptionPane.ERROR_MESSAGE);
        }                  
        
    }//GEN-LAST:event_jButtonOkActionPerformed

